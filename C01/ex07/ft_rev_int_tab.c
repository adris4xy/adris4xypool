void	ft_rev_int_tab(int *tab, int size)
{
	int a[size];
	int i;
	int k;
	int z;

	i = 0;
	k = size - 1;
	while (i < size && k >= 0)
	{
		a[i] = tab[k];
		--k;
		++i;
	}
	z = 0;
	while (z < size)
	{
		tab[z] = a[z];
		++z;
	}
}
